#include "../libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	if (!dest && !src)
		return (NULL);
	while (n--)
		((t_byte *)dest)[n] = ((t_byte *)src)[n];
	return (dest);
}
