#include "../libft.h"

void	*ft_memchr(void const *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((t_byte *)s)[i] == (t_byte)c)
			return ((void *)s + i);
		i++;
	}
	return (NULL);
}
