#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*find;

	find = NULL;
	while (*s)
	{
		if (*s == (char)c)
			find = (char *)s;
		s++;
	}
	if (!(char)c)
		return ((char *)s);
	return (find);
}
