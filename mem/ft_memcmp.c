#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (!n)
		return (0);
	while (i + 1 < n && ((t_byte *)s1)[i] == ((t_byte *)s2)[i])
		i++;
	return (((t_byte *)s1)[i] - ((t_byte *)s2)[i]);
}
